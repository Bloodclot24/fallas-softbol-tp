package com.drools;

public class Lanzador {
	private String distPeso;
	private String pies;
	private String soltadoPelota;
	private String movMunieca;
	private String traza;
	private String velocidad;
	private String lanzamiento;
	
	public void setDistPeso(String distPeso) {
		this.distPeso = distPeso;
	}
	
	public String getDistPeso() {
		return distPeso;
	}
	
	public void setPies(String pies) {
		this.pies = pies;
	}
	
	public String getPies() {
		return pies;
	}
	
	public void setSoltadoPelota(String soltadoPelota) {
		this.soltadoPelota = soltadoPelota;
	}
	
	public String getSoltadoPelota() {
		return soltadoPelota;
	}
	
	public void setMovMunieca(String movMunieca) {
		this.movMunieca = movMunieca;
	}
	
	public String getMovMunieca() {
		return movMunieca;
	}
	
	public void setTraza(String traza) {
		this.traza = traza;
	}
	
	public String getTraza() {
		return traza;
	}
	
	public void setVelocidad(String velocidad) {
		this.velocidad = velocidad;
	}
	
	public String getVelocidad() {
		return velocidad;
	}

	public void setLanzamiento(String lanzamiento) {
		this.lanzamiento = lanzamiento;
	}

	public String getLanzamiento() {
		return lanzamiento;
	}
	
	
}
