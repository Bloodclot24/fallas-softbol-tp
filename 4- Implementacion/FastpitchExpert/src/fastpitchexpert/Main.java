    private Pregunta _preguntaActual;
    private boolean _actualizando = false;
    
    private void actualizarElementos() {
        _actualizando = true;
        actualizarBotones();
        actualizarPregunta();
        _actualizando = false;
    }
    
    private void actualizarBotones() {
        if (! FastpitchExpert.getInstance().hayProxima()) 
            btnNext.setText("Ver Resultado");
        else
            btnNext.setText("Siguiente");
        btnPrev.setEnabled(FastpitchExpert.getInstance().hayPrevia());
        limpiarResultado();
    }
    
    private void actualizarPregunta() {
        question_lbl.setText(_preguntaActual.pregunta);
        question_options.removeAllItems();
        for (int i=0; i<_preguntaActual.respuestas.size(); ++i) {
            question_options.addItem(_preguntaActual.respuestas.get(i));
        }
        question_options.setSelectedIndex(_preguntaActual.respuestaElegida());
        actualizarRespuesta();
    }
    
    private void actualizarRespuesta() {
        Respuesta r = (Respuesta) question_options.getSelectedItem();
        option_description.setText(r.descripcion);
    }
    
    private void limpiarResultado() {
        btnNext.setEnabled(true);
        description_label.setText("");
        description_text.setText("");
    }
    
    private void procesarRespuestas() {
        DescrTipoLanzamiento resultado = FastpitchExpert.getInstance().obtenerResultado();
        if(resultado != null){
            description_label.setText(resultado.tipoLanzamiento);
            description_text.setText(resultado.descripcion);
            System.out.println(resultado.tipoLanzamiento);
        }
        else {
            description_label.setText("Sin Resultado");
            System.out.println("Sin Resultado");
        }
    }
    
